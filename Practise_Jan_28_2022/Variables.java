package Practise_Jan_28_2022;

public class Variables {

	public static void main(String[] args) {
		//a variable is a container that is used
		// to store some data/value
		
		//type_of_variable name_of_variable = value/data;
		
		byte age = 29;
		short salary = 20000;
		int pincode = 800001;
		long mobile_no = 9876543210l;
		
		float percentage = 92.5f;
		double random = 6726.24934849;
		
		boolean areYouMarried = false;
		
		char section = 'D';
		
		String message = "Hello everyone, I am Vikas !er3478347";
		
		System.out.println(age);
		System.out.println(salary);
		System.out.println(pincode);
		System.out.println(mobile_no);
		
		System.out.println(percentage);
		System.out.println(random);
		
		System.out.println(areYouMarried);
		System.out.println(section);
		
		System.out.println(message);
	}

}

